console.log("========= compount addition ==============");
var n1 = 10;
n1 += 5;// n1 = n1 + 5 
console.log(`Compound addition Result is: ${n1}`);

console.log("========= compound subtraction ==============");
var n1 = 10;
n1 -= 5;// n1 = n1 - 5 
console.log(`Compound subtraction Result is: ${n1}`);

console.log("========= compound multiple ==============");
var n1 = 10;
n1 *= 5;// n1 = n1 * 5 
console.log(`Compound multiple Result is: ${n1}`);

console.log("========= compound Division ==============");
var n1 = 10;
n1 /= 5;// n1 = n1 / 5 
console.log(`Compound division Result is: ${n1}`);

console.log("========= compound module ==============");
var n1 = 10;
n1 %= 5;// n1 = n1 % 5 
console.log(`Compound module Result is: ${n1}`);